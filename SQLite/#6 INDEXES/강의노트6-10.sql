INDEX를 사용해야 하는 경우와 사용하지 말아야 하는 경우 정리

✅ 인덱스를 사용해야 하는 경우
1. WHERE / ORDER BY / JOIN에 자주 사용하는 열
	• 예: WHERE user_id = 5
		→ user_id에 인덱스가 있으면 빠르게 찾음.
		
2. 고유한 값을 가진 열 (높은 카디널리티)
	• 예: 주민등록번호, 이메일, 유저 ID
		→ 값이 다양할수록 인덱스 효율 높음.
		
3. 테이블이 클 경우
	• 예: 100만 개 영화 중 특정 제목 찾기
		→ 인덱스 없으면 다 뒤져야 해서 느림.
		
4. 외래키가 있는 경우
	• 예: orders.user_id → users.id
		→ user_id에 인덱스가 있으면 조인 성능 향상.

❌ 인덱스를 신중히 써야 하는 경우
5. 모든 열마다 인덱스 만들지 않기
	• 예: 테이블에 열이 10개인데 다 인덱스 → 오히려 느림.
		→ 인덱스는 삽입/수정/삭제할 때 성능 떨어뜨림.
		
6. 작업이 완료된 후에 쿼리 최적화
	• 예: 개발 초기에 무작정 인덱스 X
		→ 실제로 느려졌을 때 인덱스 적용.
		
7. 여러 열을 함께 필터링하거나 정렬하는 경우
	• 예: WHERE country = 'KR' AND age > 30
		→ country, age 순서로 복합 인덱스 사용 가능.
		
8. 가능하다면 커버링 인덱스 사용
	• 예: SELECT name FROM users WHERE age > 20
		→ age, name으로 인덱스 만들면 테이블 접근 없이 처리됨.

🚫 인덱스를 피해야 하는 경우
9. 작은 테이블
	• 예: 데이터가 20개밖에 없음
		→ 인덱스보다 그냥 스캔하는 게 더 빠름.
		
10. 자주 변경되는 열
	• 예: last_login_time, views_count 등 자주 바뀜
		→ 인덱스 유지 비용이 큼 → 성능 저하.
		
11. 큰 텍스트 열
	• 예: 블로그 content (길이가 김)
		→ 일반 인덱스 대신 FULL-TEXT INDEX 사용해야 검색 효율적. -> MySQL에서만 가능